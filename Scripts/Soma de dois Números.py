n1 = int(input("Digite um número: ")) #chama a função de input e guarda na variável n1
n2 = int(input("Digite outro número: ")) #chama a função de input e guarda na variável n2
s = n1+n2 #Variável "s" recebe a coma de "n1" + "n2"

print("A soma de", n1, "+", n2, "é", s) #exemplo básico de concatenação com PY

print("A soma de {} + {} é {}".format(n1,n2,s)) #exemplo mais avançado de concatenação com PY
